    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:

        if (txtAtomo.getText().length() == 0) {
            JOptionPane.showMessageDialog(null, "Por favor Asegurate de ingresar un átomo", "Error!", JOptionPane.ERROR_MESSAGE);
        } else {
            if (hileraTemp.length() == 1) {
                hileraTemp += txtAtomo.getText();
            } else {
                if(hileraTemp.contains(txtAtomo.getText())){
                    JOptionPane.showMessageDialog(null, "Dato ya existe! \n Ingrese otro dato", "Error!", JOptionPane.ERROR_MESSAGE);
                    txtAtomo.setText("");
                    return;
                }
                hileraTemp += txtAtomo.getText();
                btnHermano.setEnabled(true);
            }
            labelHileraTemp.setText(hileraTemp); // escribe hilera en el label.
            txtAtomo.setText("");
            btnTermina.setEnabled(true);
            btnSubArbol.setEnabled(true);
            btnOk.setEnabled(false);
            txtAtomo.setEnabled(false);
                    
            if (subArbol == 0) {
                btnTerminaSub.setEnabled(false);
            } else {
                btnTerminaSub.setEnabled(true);
            }

        }
    }//GEN-LAST:event_btnOkActionPerformed

    private void btnSubArbolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubArbolActionPerformed
        // TODO add your handling code here:
        hileraTemp += txtAtomo.getText() + "(";
        subArbol = subArbol + 1;
        btnOk.setEnabled(true);
        txtAtomo.setEnabled(true);
        btnTermina.setEnabled(false);
        btnSubArbol.setEnabled(false);
        btnHermano.setEnabled(false);
        btnTerminaSub.setEnabled(false);

    }//GEN-LAST:event_btnSubArbolActionPerformed

    private void btnTerminaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTerminaActionPerformed
        // TODO add your handling code here:
        for (int i = subArbol; i >= 0; i--) {
            hileraTemp += ")";
        }
        labelHileraTemp.setText(hileraTemp);
        btnOk.setEnabled(false);
        btnTermina.setEnabled(false);
        btnSubArbol.setEnabled(false);
        btnHermano.setEnabled(false);
        btnTerminaSub.setEnabled(false);
        txtAtomo.setEnabled(false);
        this.setVisible(false);
    }//GEN-LAST:event_btnTerminaActionPerformed

