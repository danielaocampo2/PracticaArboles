    private void btnTerminaSubActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTerminaSubActionPerformed
        // TODO add your handling code here:

        hileraTemp += ")";
        subArbol = subArbol - 1;
        labelHileraTemp.setText(hileraTemp);
        btnOk.setEnabled(false);
        btnTermina.setEnabled(true);
        btnSubArbol.setEnabled(true);
        btnHermano.setEnabled(true);
        btnTerminaSub.setEnabled(true);
        if (subArbol == 0) {
            btnTerminaSub.setEnabled(false);
            btnHermano.setEnabled(false);
            btnSubArbol.setEnabled(false);
        }
    }//GEN-LAST:event_btnTerminaSubActionPerformed

